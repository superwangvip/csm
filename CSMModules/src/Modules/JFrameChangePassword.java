    public int save_update_info() {
        int ret_status = 0;
        String ret_message = "保存成功";
        PreparedStatement SqlStatement;

        if (OLD_PWD && NEW_PWD) {
            try {
                int operator = 1;
                int user_id = JFrameLogin.user_id;

                char[] new_password1 = jPasswordField2.getPassword();
                String new_password = String.valueOf(new_password1);
                SqlStatement =
                        Main.conn.prepareStatement("execute reset_password ?,?,?");

                SqlStatement.setInt(1, operator);
                SqlStatement.setInt(2, user_id);
                SqlStatement.setString(3, new_password);

                boolean HasResult = SqlStatement.execute();

                if (!HasResult) {
                    HasResult = SqlStatement.getMoreResults();
                }

                if (HasResult) {
                    ResultSet SqlResult = SqlStatement.getResultSet();
                    while (SqlResult.next()) {
                        ret_status = SqlResult.getInt("ret_status");
                        ret_message =
                                SqlResult.getString("ret_msg");
                    }
                }

            } catch (SQLException ex) {
                Logger.getLogger(JFrameServiceCallback.class.getName()).log(Level.SEVERE, null, ex);
                Main.logger.warning(ex.getMessage());
                JOptionPane.showMessageDialog(this, ex.getLocalizedMessage());
            }

        } else {
            ret_message = "操作不正确";
        }

        JOptionPane.showMessageDialog(this, ret_message);
        OLD_PWD = false;
        NEW_PWD = false;
        return ret_status;
    }

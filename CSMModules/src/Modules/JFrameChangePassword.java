    private void jPasswordField3FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jPasswordField3FocusLost
        char[] newPassword1 = jPasswordField2.getPassword();
        String newPassword = String.valueOf(newPassword1);
        char[] surePassword1 = jPasswordField2.getPassword();
        String surePassword = String.copyValueOf(surePassword1);
        if (newPassword.compareTo(surePassword) != 0) {
            JOptionPane.showMessageDialog(this, "新密码两次输入不符！");
            jPasswordField1.setText("");
            jPasswordField2.setText("");
            NEW_PWD = false;
            return;
        } else {
            NEW_PWD = true;
        }
    }//GEN-LAST:event_jPasswordField3FocusLost


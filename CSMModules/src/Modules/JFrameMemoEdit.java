    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (operationType == NO_ACTION) {
            JOptionPane.showMessageDialog(this, "你没有操作或者最近操作已经保存");
            return;
        }
        int ret_status = 0;
        String ret_message = "保存成功";
        String memo = jTextArea1.getText();
        PreparedStatement SqlStatement;
        try {
            SqlStatement = Main.conn.prepareStatement("execute add_memo ?,?,?");
            SqlStatement.setLong(1, this.fund_account);
            SqlStatement.setString(2, this.memo_type);
            SqlStatement.setString(3, memo);
            Boolean HasResult = SqlStatement.execute();

            while (!HasResult) {
                HasResult = SqlStatement.getMoreResults();
            }
            if (HasResult) {

                ResultSet SqlResult = SqlStatement.getResultSet();
                while (SqlResult.next()) {
                    ret_status = SqlResult.getInt("ret_status");
                    ret_message = SqlResult.getString("ret_msg");//????
                }
                JOptionPane.showMessageDialog(this, ret_message);
            }

        } catch (SQLException ex) {
            Logger.getLogger(JFrameMemoEdit.class.getName()).log(Level.SEVERE, null, ex);
            Main.logger.warning(ex.getLocalizedMessage());
            JOptionPane.showMessageDialog(this, ex.getLocalizedMessage());
        }
        listMemo();

    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int r = jTable1.getSelectedRow();
        if (r == -1) {
            JOptionPane.showMessageDialog(this, "请先选择要删除的记录（用鼠标点击记录)");
            return;
        }
                TableModel m = jTable1.getModel();
        int mr = jTable1.convertRowIndexToModel(r);
        String memo_id_s = m.getValueAt(mr, 0).toString();
        int memo_id = Integer.valueOf(memo_id_s);

        if (JOptionPane.showConfirmDialog(this, "确信删除序号为:" + memo_id_s + "的备注?") != JOptionPane.YES_OPTION) {
          return;
        }
        operationType = DELETE_MEMO;

        int ret_status = 0;
        String ret_message = "删除成功";
        String memo = jTextArea1.getText();
        PreparedStatement SqlStatement;
        try {
            SqlStatement = Main.conn.prepareStatement("execute delete_memo ?,?");
            SqlStatement.setLong(1, this.fund_account);
            SqlStatement.setInt(2, memo_id);

            Boolean HasResult = SqlStatement.execute();
            while (!HasResult) {
                HasResult = SqlStatement.getMoreResults();
            }
            if (HasResult) {

                ResultSet SqlResult = SqlStatement.getResultSet();
                while (SqlResult.next()) {
                    ret_status = SqlResult.getInt("ret_status");
                    ret_message = SqlResult.getString("ret_msg");//????
                }
                JOptionPane.showMessageDialog(this, ret_message);
            }

        } catch (SQLException ex) {
            Logger.getLogger(JFrameMemoEdit.class.getName()).log(Level.SEVERE, null, ex);
            Main.logger.warning(ex.getLocalizedMessage());
            JOptionPane.showMessageDialog(this, ex.getLocalizedMessage());
        }
        listMemo();

    }//GEN-LAST:event_jButton2ActionPerformed

    private void listMemo() {
        JDBTableModel dm = new JDBTableModel(jTable1);
        Vector columnType = new Vector();
        boolean addSerial = false;
        String sql = "list_memo " + this.fund_account + ", " + this.memo_type;
        dm.fetchDataToTable(Main.conn, sql, columnType, addSerial);
    }


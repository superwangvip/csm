    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int selectedRow = jTable1.getSelectedRowCount();
        if (selectedRow == 0) {
            JOptionPane.showMessageDialog(this, "请先选择要删除的记录");
            return;
        }
        if (JOptionPane.showConfirmDialog(this, "确信删除所选消息记录") == JOptionPane.YES_OPTION) {
            int[] selectedRowNo = jTable1.getSelectedRows();
            TableModel md = jTable1.getModel();
            for (int r = 0; r < selectedRow; r++) {
                int mr = jTable1.convertRowIndexToModel(selectedRowNo[r]);
                String message_id_s = md.getValueAt(mr, 1).toString();
                int message_id_ = Integer.valueOf(message_id_s);
                deleteMessage(message_id_);
            }
        }
        JOptionPane.showMessageDialog(this, "消息已删除");
        fetchMessageList();
    }//GEN-LAST:event_jButton1ActionPerformed


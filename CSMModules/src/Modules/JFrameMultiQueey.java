    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (!hasData) {
            JOptionPane.showMessageDialog(this, "没有数据需要保存!");
            return;
        }

        String outputFile = table_name + ".xls";
        pathExist(outPath);//检查并创建文件输出目录
        String outputFile1 = outPath + "\\" + outputFile;
        JTableToExcel tbexcel = new JTableToExcel();
        try {
            tbexcel.saveToExcel(jTable2, columnType, outputFile1, addSerial);
            String msg = outputFile1 + " : 已保存";
            JOptionPane.showMessageDialog(this, msg);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(JFrameClientProductList.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(JFrameClientProductList.class.getName()).log(Level.SEVERE, null, ex);
            Main.logger.warning(ex.getLocalizedMessage());
            JOptionPane.showMessageDialog(this, ex.getLocalizedMessage());
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void dateSelect(JTextField jText, String format) {
        Point mousepoint = MouseInfo.getPointerInfo().getLocation();
        System.out.println(mousepoint.x + "   " + mousepoint.y);
        JFrame jf = new JFrame("Calendar");
        jf.setTitle("万年历");
        jf.setBounds(mousepoint.x - 20, mousepoint.y + 20, 180, 100);
        jf.setVisible(true);
        DateChooser mp = new DateChooser(jf, jText, format);
        jf.add(mp, BorderLayout.CENTER);
        jf.add(new JButton("点击下面日期打开日历"), BorderLayout.NORTH);
    }

    //检查并创建目录
    public void pathExist(String path) {
        File file = new File(path);
        //判断文件夹是否存在,如果不存在则创建文件夹
        if (!file.exists()) {
            file.mkdir();
            System.out.println(path + "--目录已经建立");
        } else {
            System.out.println(path + "--目录已经存在");
        }
    }

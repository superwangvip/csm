    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (!hasData) {
            JOptionPane.showMessageDialog(this, "没有数据需要保存!");
            return;
        }
        TableModel md = jTable1.getModel();
        String outputFile = "经纪人客户佣金" + getDateString() + ".xls";

        JTableToExcel tbexcel = new JTableToExcel();
        try {
            tbexcel.saveToExcel(jTable1, columnType, outputFile, addSerial);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(JFrameSql.class.getName()).log(Level.SEVERE, null, ex);
            Main.logger.warning(ex.getMessage());
        } catch (IOException ex) {
            Logger.getLogger(JFrameAgentReport.class.getName()).log(Level.SEVERE, null, ex);
            Main.logger.warning(ex.getMessage());
        }
        String msg = outputFile + " 已保存到工作目录下!";
        JOptionPane.showMessageDialog(this, msg);

    }//GEN-LAST:event_jButton3ActionPerformed
    String getDateString() {
        String date = "1900";
        GregorianCalendar d = new GregorianCalendar();
        int thisYear = d.get(Calendar.YEAR);
        int thisMonth = d.get(Calendar.MARCH) + 1;
        int thisDay = d.get(Calendar.DATE);
        date = String.format("%4d%02d%02d", thisYear, thisMonth, thisDay);
        return date;
    }
